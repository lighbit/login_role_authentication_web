package com.zulkarnaen.login.model;

public class Form4Model {
	
	private String namaAgen, noIdAgen, jenisAgen, lokasiAgen, branchCode, bankCode, city;

	
	public String getBankCode() {
		return bankCode;
	}

	public void setBankCode(String bankCode) {
		this.bankCode = bankCode;
	}

	public String getNamaAgen() {
		return namaAgen;
	}

	public void setNamaAgen(String namaAgen) {
		this.namaAgen = namaAgen;
	}

	public String getNoIdAgen() {
		return noIdAgen;
	}

	public void setNoIdAgen(String noIdAgen) {
		this.noIdAgen = noIdAgen;
	}

	public String getJenisAgen() {
		return jenisAgen;
	}

	public void setJenisAgen(String jenisAgen) {
		this.jenisAgen = jenisAgen;
	}

	public String getLokasiAgen() {
		return lokasiAgen;
	}

	public void setLokasiAgen(String lokasiAgen) {
		this.lokasiAgen = lokasiAgen;
	}

	public String getBranchCode() {
		return branchCode;
	}

	public void setBranchCode(String branchCode) {
		this.branchCode = branchCode;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}
	
	

}
